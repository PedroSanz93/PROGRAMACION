//cuenta hacia atras desde 320 hasta 160 de 20 en 20 con DO WHILE
package EJERCICIOS_BUCLES;

public class Ejercicio06 {

    public static void main(String[] args) {
        int i=320;
        do
        {
            i=i-20;
            System.out.println(i);
        }
        while (i>160);
    }
}
