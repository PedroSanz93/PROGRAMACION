//cuenta hacia adelante de multiplos de 5 hasta 100 con DO WHILE
package EJERCICIOS_BUCLES;

public class Ejercicio03 {

    public static void main(String[] args) {
        int i=0;
        do
        {
        i=i+5;
        System.out.println(i);
        }
        while (i<100);
    }
}
