
package trimestre_2;

public class Repaso_18_Bucles_DoWhile {

    public static void main(String[] args) {
        int i=1;
        System.out.println("Inicio: ");
        do{
            System.out.println(i);
            i++;
        } while (i<=10);
        System.out.println("Fin");
    }
    
}
